# ---------------------------------------------------------------------------
# Гиперпараметры модели
# ---------------------------------------------------------------------------
MODEL_CONFIG = {
    # d_model — размерность каждого токена (эмбеддинга).
    #   Все признаки и категория проецируются в пространство этой размерности.
    #   Чем больше d_model, тем выразительнее модель, но медленнее обучение.
    #   Должен делиться на nhead без остатка (d_model % nhead == 0).
    #   Примеры: 32 (лёгкая), 64 (базовая), 128 (мощнее, ~4× больше параметров)
    "d_model": 64,

    # nhead — число голов внимания (Multi-Head Attention).
    #   Каждая голова учится замечать разные взаимосвязи между признаками.
    #   Должно быть делителем d_model: при d_model=64 допустимо 1, 2, 4, 8.
    #   Примеры: 2 (минимум), 4 (базовое), 8 (больше нюансов, нужен d_model≥64)
    "nhead": 4,

    # num_layers — число слоёв Transformer Encoder (глубина модели).
    #   Больше слоёв = модель улавливает более сложные зависимости между признаками,
    #   но растёт риск переобучения на малых данных и время обучения.
    #   Примеры: 1–2 (мало данных / быстро), 3 (базовое), 4–6 (много данных)
    "num_layers": 3,

    # dim_feedforward — размер скрытого слоя FFN внутри каждого Transformer-блока.
    #   Обычно выбирают 2×–4× от d_model.
    #   Примеры: 128 (2× d_model, базовое), 256 (4× d_model, мощнее)
    "dim_feedforward": 128,

    # dropout — вероятность обнуления нейронов при обучении (регуляризация).
    #   Снижает переобучение. При малом датасете увеличьте, при большом — уменьшите.
    #   Примеры: 0.0 (без регуляризации), 0.1 (базовое), 0.2–0.3 (сильная регуляризация)
    "dropout": 0.1,

    # category_emb_dim — размерность эмбеддинга категории товара.
    #   Должен совпадать с d_model, чтобы категориальный токен имел ту же размерность,
    #   что и числовые токены (все складываются в один тензор перед Transformer).
    #   Меняйте вместе с d_model.
    "category_emb_dim": 64,  # должен совпадать с d_model
}

